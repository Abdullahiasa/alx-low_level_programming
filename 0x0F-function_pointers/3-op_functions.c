/**
 * add - Add 2 integers
 *
 * @a: int 1
 * @b: int 2
 *
 * Return: Integer
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - Subtract 2 integers
 *
 * @a: int 1
 * @b: int 2
 *
 * Return: Integer
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * multi - multiplies 2 integers
 *
 * @a: int 1
 * @b: int 2
 *
 * Return: Integer
 */
int multi(int a, int b)
{
	return (a * b);
}

/**
 * divi - divide 2 integers
 *
 * @a: int 1
 * @b: int 2
 *
 * Return: Integer
 */

int divi(int a, int b)
{
	return (a / b);
}

/**
 * remi - gives remainder division
 *
 * @a: int 1
 * @b: int 2
 *
 * Return: Integer
 */

int remi(int a, int b)
{
	return (a % b);
}
